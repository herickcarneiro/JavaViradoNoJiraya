package maranotajava.introducao;

public class EstruturasDeRepetição {
    public static void main(String[] args) {
        // while, do-while, for
        int count = 12;
        while(count < 10){
            System.out.println(++count);
        }
        do{
            System.out.println(++count);
        }while(count < 10);

    }
}
